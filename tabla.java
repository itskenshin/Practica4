     public void mostrarDataos(){
        //Preprando mis registros en mi tabla...
        while(modelo.getRowCount()>0){
            modelo.removeRow(0);
        }
        //Estableciendo conexi√≥n en mi base de datos.
        conexion objConexion= new conexion();
        //Try catch para el manejo de posibles errores.
          try {
            //Realizando consulta para traer todos los registros que esten en mi tabla usuario de mi base de datos sistema.
            ResultSet resultado = objConexion.consultarRegistros("SELECT * FROM usuarios");
            //Tomando todos los registros y guardandolo en un object para luego mostarlos en mi tabla del programa...
            while (resultado.next()) {
                System.out.println(resultado.getString("id"));
                System.out.println(resultado.getString("user"));
                System.out.println(resultado.getString("password"));
                System.out.println(resultado.getString("email"));
                System.out.println(resultado.getString("nombre"));
                System.out.println(resultado.getString("apellido"));
                System.out.println(resultado.getString("telefono"));
                Object[] oUsuario={resultado.getString("id"),resultado.getString("user"),resultado.getString("password"),resultado.getString("email"),resultado.getString("nombre"),resultado.getString("apellido"),resultado.getString("telefono")};
                modelo.addRow(oUsuario);
            }
        } catch (Exception e) {
            System.out.println(e);
        }
        
        
    }
    
